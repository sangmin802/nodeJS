// Express : NodeJs를 좀 더 편리하게 사용할 수 있도록, 자주 사용되는 기능들을 API로 만들어 제공하는 프레임워크이다.
// ex) 굳이 따지자면, JS의 jQuery, vue, Angular, React와 같은기능

// 0. express.js 검색 후, 해당 사이트에 접속하면 npm install express --save문구가 있음
const express = require('express');
const app = express();
// 1. express라는 모듈은 method 이며, 해당 method의 return값은 application이라는 객체이다.

app.get('/', (req, res) => res.send('Hello World!'));
// 2. application객체의 get method는 첫번째 인자로 경로를 받으며, 두번째 인자로 콜백함수를 받는다.
// 3. get method는 경로를 통해 나눠지는? 이동하는 route, routing 기능이다.
// 4. 우리는 기존 nodeJs에서 routing기능은 pathname을 if문에 걸어 진행하였다.

app.listen(3000, () => console.log(`Example app listening on port 3000!`));

